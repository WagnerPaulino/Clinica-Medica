    private void jbPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarActionPerformed
        try {

            PacienteDAO clienteDAO = new PacienteDAO();
            cliente = clienteDAO.buscarUmCliente(jftfCpfPesquisa.getText());

            jtfNome.setText(cliente.getNome());

            jftfCpfPesquisa.setText("");
            if(cliente == null){
                

                jftfCpfPesquisa.setText("");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Erro ao pesquisar:\n " +  e);
        }
    }//GEN-LAST:event_jbPesquisarActionPerformed

    private void jbNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbNovoActionPerformed

        try {

            TelaCadastroCliente telacadcli = new TelaCadastroCliente();
            TelaPrincipalMDi.TelaPrincipal1.add(telacadcli);
            telacadcli.setVisible(true);

            jftfCpfPesquisa.setText("");

            jtfNome.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, " Erro ao alterar dados do cliente:\n " + e);
        }finally{
            this.cliente = new Paciente();
        }
    }//GEN-LAST:event_jbNovoActionPerformed

