    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed

        try{
             MedicoDAO medicoDAO = new MedicoDAO();
            Usuario usuario = new Usuario();
           medico = medicoDAO.buscarUmMedico(Integer.parseInt(jtfPesquisarMedico.getText()));
            jtfNome.setText(medico.getNome());
            jcbEspecialidade.setSelectedItem(medico.getEspecialidade());
            jtfCPF.setText(usuario.getCpf());
            jtfDatadeNascimento.setText(usuario.getDtNascimento());
            jtfPesquisarMedico.setText("");
           
            
            medicoDAO.save(medico);
            JOptionPane.showMessageDialog(this, "Dados da roupa salvados com sucesso !");
            jtfPesquisarMedico.setText("");
            jtfNome.setText("");
            jcbEspecialidade.setSelectedItem("Selecione");
            jtfCPF.setText("");
            jtfDatadeNascimento.setText("");
            jtfCrm.setText("");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, " Erro ao atualizar dados do cliente:\n " + e);
        }finally{
            this.medico = new Medico();
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

